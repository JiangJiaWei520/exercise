class exercise_profit:
    def bonus(self):
        i = int(input("请输入利润金额(万元)："))
        r = 0
        profitList = [100, 60, 40, 20, 10,0]
        commissionList = [0.01, 0.015, 0.03, 0.05, 0.075, 0.1]
        """
            i=3
               x=5  3>0 r=0+(3-0)*0.1
                        i=0,r=0.3
            i=11
              x=4   11>10 r=r=0+(11-10)*0.1
                         i=1,r=1
              x=5   1>0 r=r=1+(1-0)*0.075
                         i=0,r=0.075
            i=110
              x=0   110>100 r=0+(110-100)*0.01
                            i=100,r=0.10
              x=1   100>60  r=0.1+(100-60)*0.015
                            i=60,r=0.70
              x=2   60>40  r=0.70+(60-40)*0.03
                            i=40,r=1.3
              x=3   40>20  r=1.3+(40-20)*0.05
                            i=20,r=2.3
              x=4   20>10  r=2.3+(20-10)*0.075
                            i=10,r=3.05
              x=5   10>0  r=3.05+(10-0)*0.1
                            i=0,r=4.05
        """
        for x in range(0,6):
            if i>profitList[x]:
                r+=(i-profitList[x])*commissionList[x]
                i=profitList[x]
        print(str(r)+"(万元)")



